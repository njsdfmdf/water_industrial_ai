import pandas as pd

# 准备数据 (字典格式：Key是列名, Value是数据列表)
data = {
    '姓名': ['张三', '李四', '王五', '赵六'],
    '年龄': [23, 22, 25, 22],
    '城市': ['北京', '上海', '深圳', '北京'],
    '分数': [88.5, 92.0, 79.5, 95.0]
}

# 变成 DataFrame
df = pd.DataFrame(data) 
# 或者更常见的 pd.DataFraame(data)

print("--- 原始表格 ---")
print(df)

print("------------------ 查看信息 -------------------")
# 查看前 2 行 (数据量大时很有用)
print(df.head(2))

# 查看数据的基本信息 (有没有空值，是什么类型)
print(df.info())

# 快速查看统计数据 (平均值、最大最小值等)
print(df.describe())

print("----------------- 选择和筛选 -----------------")
# 1. 取某一列 (返回的是 Series)
print(df['姓名'])

# 2. 筛选：找出分数大于 90 的人
high_score = df[df['分数'] > 90]
print("--- 高分同学 ---")
print(high_score)

# 3. 多重筛选：找出 "北京" 的且 "年龄小于 23" 的人
young_bj = df[(df['城市'] == '北京') & (df['年龄'] < 23)]
print("--- 北京的年轻同学 ---")
print(young_bj)

print("-------------------- 修改数据 -------------------")
# 新增一列：假设每个人加 2 分
df['加分后'] = df['分数'] + 2

# 修改某一列：把 '城市' 里的 '北京' 改成 'Beijing'
df.loc[df['城市'] == '北京', '城市'] = 'Beijing'

print("--- 修改后的表格 ---")
print(df)
